# Приведи строку к одному регистру (например, к нижнему), чтобы избежать различий между заглавными и строчными буквами.
# Удали из строки все пробелы, знаки пунктуации и специальные символы, оставив только буквы и цифры.
# Сравни строку и её инвертированную версию.
# Если совпали, то строка является палиндромом.
# Если не совпали, строка не является палиндромом.
# Верни результат проверки — «палиндром» или «не палиндром».

def check_palindrome(func):
    def wrapper(*args):
        for s in args:
            result = func(s)
            print(f"Строка '{s}': {'палиндром' if result else 'не палиндром'}")
    return wrapper


@check_palindrome
def is_palindrome(s):
    s = s.lower().replace(' ', '').replace('.', '').replace(',', '').replace('!', '').replace('?', '')
    return s == s[::-1]


# Пример вызова
is_palindrome("А роза упала на лапу Азора", "Madam", "Hello", "12321", "Python", "A man, a plan, a canal, Panama")
